    raw_insights = tpl.get("theme_insights") or {}
    theme_insights = {
        "relationships": raw_insights.get("relationships") or [],
        "work": raw_insights.get("work") or [],
        "partner": raw_insights.get("partner") or [],
    }
    return {
        "type_name": soul_type_name,
        "one_liner": tpl.get("one_liner", fallback_summary),
        "common_issues": tpl.get("common_issues", ""),
        "why_misaligned": tpl.get("why_misaligned", ""),
        "if_ignored": tpl.get("if_ignored", ""),
        "small_step": tpl.get("small_step", ""),
        "hard_alone": tpl.get("hard_alone", ""),
        "course_fit": tpl.get("course_fit", ""),
        "theme_insights": theme_insights,
    }


