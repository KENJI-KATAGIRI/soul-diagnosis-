def score_themes(answers: Dict[str, int], questions: List[Any]) -> Dict[str, int]:
    """q の theme_weights からテーマ別の生スコア（中央3をゼロ、強い同意ほどプラス）。"""
    themes: Dict[str, int] = {k: 0 for k in THEME_KEYS}
    for q in questions:
        tw = getattr(q, "theme_weights", None) or {}
        if not tw:
            continue
        a = answers.get(q.key, 3)
        try:
            a = int(a)
        except (TypeError, ValueError):
            a = 3
        if a not in {1, 2, 3, 4, 5}:
            a = 3
        intensity = a - 3
        for tkey, w in tw.items():
            if tkey in themes:
                themes[tkey] += int(w) * intensity
    return themes


def _pick_theme_insights(theme_scores: Dict[str, int], *, max_items: int = 3, min_score: int = 3) -> List[Tuple[str, str, str]]:
    """
    (theme_key, title, body) のリスト。min_score 以上のテーマを優先し、
    なければ上位を緩和（>=2）、それでもなければ空。
    """
    ranked = sorted(theme_scores.items(), key=lambda kv: kv[1], reverse=True)
    positive = [(k, s) for k, s in ranked if s >= min_score]
    if not positive:
        positive = [(k, s) for k, s in ranked if s >= 2]
    if not positive:
        return []
    out: List[Tuple[str, str, str]] = []
    for k, _s in positive[:max_items]:
        title, body = THEME_COPY.get(k, ("", ""))
        if title and body:
            out.append((k, title, body))
    return out


def build_manuscript_insight(
    *,
    phase_key: str,
    best_type_key: str,
    theme_scores: Dict[str, int],
) -> Dict[str, object]:
    """結果テンプレ用: フェーズ×タイプ本文 + テーマ別パラグラフ。"""
    by_phase = PHASE_TYPE_BODY.get(phase_key) or {}
    phase_type_body = by_phase.get(best_type_key) or (
        "いまの変容フェーズと魂タイプの組み合わせから、身体感覚と選択のリズムをセットで整えていくことが大切です。"
        "上記の『いまの位置』の説明と、タイプ別の傾向をあわせて読んでください。"
    )

    theme_items = _pick_theme_insights(theme_scores)
    theme_neutral_message = ""
    if not theme_items:
        theme_neutral_message = (
            "後半の設問（使命・統合・自己責めなど）からは、特定のテーマが突出してはいません。"
            "フェーズ・ナビの寄り・魂タイプの読みを中心に、ゆっくり味わってみてください。"
        )

    return {
        "phase_type_body": phase_type_body,
        "theme_items": theme_items,
        "theme_neutral_message": theme_neutral_message,
    }
